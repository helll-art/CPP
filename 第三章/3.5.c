//如果输入浮点型数据 输出也一定是浮点型
//即使想要整形 就把%lf改成%.0lf
#include<stdio.h>
int main()
{
    double a;
    scanf("%lf",&a);
    printf("您的年龄合计%.0lf秒。\n",a*3.156e7);
    return 0;
}
