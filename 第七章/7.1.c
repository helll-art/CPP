#include<stdio.h>
int main(void)
{
    char ch;
    int a[3]={0};
    while((ch=getchar())!='#')
    {
        if(ch==' ') a[0]++;
        else if (ch=='\n') a[1]++;
        else a[2]++;

    }
    printf("空格数：%d\n换行符数:%d\n其他字符数：%d\n",a[0],a[1],a[2]);
}
