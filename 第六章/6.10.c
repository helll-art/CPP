//注意：下标不要越界（在第二个for循环  i应该从7开始递减 而不是8）
#include<stdio.h>
int main(void)
{
    int a[8];
    for(int i=0;i<8;i++)
    {
        scanf("%d",&a[i]);
    }
    for (int i=7;i>=0;i--)
    {
        printf("%d\t",a[i]);
    }
}
