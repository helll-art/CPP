#include<stdio.h>
int main(void)
{
    char a[26];
    for (int i=0;i<26;i++)
    {
        a[i]='A'+i;
        printf("%c\t",a[i]);
        if((i+1)%5 == 0)
        {
            printf("\n");
        }
    }
}

