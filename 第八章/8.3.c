#include<stdio.h>
int main(void)
{
    char ch;
    int d=0,x=0;
    for(int i=0;(ch=getchar())!=EOF;i++)
    {
        if( ch>='A'&&ch<='Z' )
            d++;
        if( ch>='a'&&ch<='z' )
            x++;
    }
    printf("大写字母个数：%d\n小写字母个数:%d\n",d,x);
}
