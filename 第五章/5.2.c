#include<stdio.h>
int main(void)
{
    int a,cnt=0;
    printf("请输入数据：");
    scanf("%d",&a);
    while(cnt<10)
    {
        printf("%d\t",a);
        if((cnt+1)%5==0)
            printf("\n");
        cnt++;
        a++;
    }
}
